def main():
    x=4
    y=2
    print("Bitwise AND X&Y=",   x&y     )
    print("Bitwise OR X|Y=",    x|y    )
    print("Bitwise XOR X^Y=",    x^y    )
    print("Bitwise Complement ~X=", ~x       )
    print("Bitwise Left Shift X<<Y=", x<<y       )
    print("Bitwise Right Shift X>>Y=", x>>y       )

if __name__ == '__main__':
    main()
